/**
 * Writes root supabase-config.js from env (for Vercel CI).
 * Local: if you already have supabase-config.js (gitignored), it is left unchanged
 * unless MADHU_SUPABASE_URL and MADHU_SUPABASE_ANON_KEY are both set (then overwrites).
 */
"use strict";

var fs = require("fs");
var path = require("path");

var root = path.join(__dirname, "..");
var target = path.join(root, "supabase-config.js");

var url = String(process.env.MADHU_SUPABASE_URL || "").trim();
var key = String(process.env.MADHU_SUPABASE_ANON_KEY || "").trim();
var onVercel = process.env.VERCEL === "1";

if (!url || !key) {
  if (onVercel) {
    console.warn(
      "[write-supabase-config] VERCEL build but MADHU_SUPABASE_URL / MADHU_SUPABASE_ANON_KEY missing — Supabase will not work until you add them in Vercel → Settings → Environment Variables."
    );
  } else if (fs.existsSync(target)) {
    console.log("[write-supabase-config] Keeping existing supabase-config.js (local).");
    process.exit(0);
  }
}

var body =
  "// Generated by scripts/write-supabase-config.js — do not commit secrets.\n" +
  "window.MADHU_SUPABASE_URL = " +
  JSON.stringify(url) +
  ";\n" +
  "window.MADHU_SUPABASE_ANON_KEY = " +
  JSON.stringify(key) +
  ";\n";

fs.writeFileSync(target, body, "utf8");
console.log("[write-supabase-config] Wrote " + path.basename(target));
